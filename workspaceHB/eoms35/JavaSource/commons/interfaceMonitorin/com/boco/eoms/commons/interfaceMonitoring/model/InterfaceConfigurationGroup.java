package com.boco.eoms.commons.interfaceMonitoring.model;

public class InterfaceConfigurationGroup {
	private String id;
	private String deleted;
	private String moduleName ;
	private String moduleDesignation;
	private String moduleId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDeleted() {
		return deleted;
	}
	public void setDeleted(String deleted) {
		this.deleted = deleted;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getModuleDesignation() {
		return moduleDesignation;
	}
	public void setModuleDesignation(String moduleDesignation) {
		this.moduleDesignation = moduleDesignation;
	}
	public String getModuleId() {
		return moduleId;
	}
	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}

	
	
	
	
	
	
	
	
}
