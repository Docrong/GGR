package com.boco.eoms.util;

public class AttachRef {
    private String attachName;//附件名
    private String attachURL;//附件地址
    private String attachLength;//附件大小

    public String getAttachName() {
        return attachName;
    }

    public void setAttachName(String attachName) {
        this.attachName = attachName;
    }

    public String getAttachURL() {
        return attachURL;
    }

    public void setAttachURL(String attachURL) {
        this.attachURL = attachURL;
    }

    public String getAttachLength() {
        return attachLength;
    }

    public void setAttachLength(String attachLength) {
        this.attachLength = attachLength;
    }
}

